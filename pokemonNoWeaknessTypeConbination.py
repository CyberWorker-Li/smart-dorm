# ===== 正确算法 =====
results = []

# 1. 计算逻辑
for mask in range(1, 1 << 18):
    defense_types = [i for i in range(18) if (mask >> i) & 1]
    has_weakness = False
    
    for atk in range(18):
        mult = 1.0
        for df in defense_types:
            mult *= table[atk][df]
        # 乘完当前攻击属性对应的全部防守抗性，再进行弱点结算
        if mult > 1.0001:
            has_weakness = True
            break
            
    if not has_weakness:
        results.append(defense_types)

# 2. 排序（按属性数量从小到大）
results.sort(key=lambda x: (len(x), x))

# 3. 写入文本文件
output_filename = "pokemon_no_weakness_combinations.txt"
with open(output_filename, "w", encoding="utf-8") as f:
    f.write(f"找到 {len(results)} 个无弱点组合\n\n")
    for combo in results:
        names = [type_names[i] for i in combo]
        f.write(f"{len(combo)} 属性: {' + '.join(names)}\n")

print(f"计算完毕！结果已成功导出至文件：{output_filename}")